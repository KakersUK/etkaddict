<a id="powerlevel"></a>
<div class="col-md-8 col-md-offset-2">
    <div class="panel panel-primary">
        <div class="panel-heading">Power Level</div>
        <div class="panel-body">
            <p>What is power level?</p>
            <p>Your power level is how your strength is calculated in ETK, it is based on your total spent XP.
            <p>Example:<br /> If you are level 98, your level will be used as your power level.</p>
            <p>However once you are level 99, the character's total XP cost of vita, mana and stat points will be taken into consideration. 
            A character with a power level of 3,384 means their total spent XP is 3.384b.</p> 
        </div>
